import numpy as np
# Create a 10 element array of random integers between 0 and 10
arr = np.random.randint(0, 10, 10)
print( "Sum is : ", arr.sum()) # sum of all elements
print( "Mean is : ", arr.mean()) # mean of all elements
print( "Max is : ", arr.max()) # maximum element
print( "Min is : ", arr.min()) # minimum element
print( "Standard Deviation is : ", arr.std()) # standard deviation